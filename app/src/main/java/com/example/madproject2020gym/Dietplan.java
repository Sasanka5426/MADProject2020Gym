package com.example.madproject2020gym;

public class Dietplan {
    private String dietEmail;
    private String diet;
    private String dietSolution;
    private String dietProcess;
    private String dietDescription;

    public Dietplan() {

    }

    public String getDietEmail() {

        return dietEmail;
    }

    public void setDietEmail(String dietEmail) {
        this.dietEmail = dietEmail;
    }

    public String getDiet() {
        return diet;
    }

    public void setDiet(String diet) {
        this.diet = diet;
    }

    public String getDietSolution() {
        return dietSolution;
    }

    public void setDietSolution(String dietSolution) {
        this.dietSolution = dietSolution;
    }

    public String getDietProcess() {
        return dietProcess;
    }

    public void setDietProcess(String dietProcess) {
        this.dietProcess = dietProcess;
    }

    public String getDietDescription() {
        return dietDescription;
    }

    public void setDietDescription(String dietDescription) {
        this.dietDescription = dietDescription;
    }


}
